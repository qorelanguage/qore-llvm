#include "qore/in/Interpreter.h"

#include <vector>
#include "qore/in/FunctionInterpreter.h"

namespace qore {
namespace in {

void interpret(Function &f) {
    Frame frame(f.getTempCount(), f.getLocalVariables().size());
    FunctionInterpreter fi(frame, f.getEntryBlock());
    fi.run();
}

} // namespace in
} // namespace qore
